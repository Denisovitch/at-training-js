let myString = "Denis"; //string
let myLogic = true; //boolean
let myNumber = 5; //number

//Выполнить сложение различных типов(string+boolean, string+number, number+boolean)
let resA1 = myString + myLogic;
console.log("🚀String+boolean = ", resA1);

let resA2 = myString + myNumber;
console.log("🚀string+number = ", resA2);

let resA3 = myNumber + myLogic;
console.log("🚀number+boolean = ", resA3);

//Выполнить умножение различных типов(string * boolean, string * number, number * boolean)
let resB1 = myString * myLogic;
console.log("🚀string * boolean = ", resB1);

let resB2 = myString * myNumber;
console.log("🚀string * number = ", resB2);

let resB3 = myNumber * myLogic;
console.log("🚀number * boolean = ", resB3);

//Выполнить деление различных типов(string/boolean, string/number, number/Boolean)
let resC1 = myString / myLogic;
console.log("🚀string/boolean = ", resC1);

let resC2 = myString / myNumber;
console.log("🚀string/number = ", resC2);

let resC3 = myNumber / myLogic;
console.log("🚀number/Boolean = ", resC3);
